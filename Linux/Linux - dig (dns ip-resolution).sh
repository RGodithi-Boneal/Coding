dig test.com ANY      /*   All DNS Information    */
dig test.net +short   /*   Only get destnation IP */
dig test.net A        /*   A Record Information   */
dig test.com MX       /*   MX (mail) Records      */
dig test.com TXT      /*   TXT  Records           */

